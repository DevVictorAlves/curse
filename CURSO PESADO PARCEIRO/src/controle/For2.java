package controle;

public class For2 {

	public static void main(String[] args) {
		// ou -= para decremento ou -- antes ou depois
		for(int contador = 20; contador >= 0; contador--) {
			System.out.println("i = " + contador);
			contador--;
			
		}
		
		// ou -= para decremento ou -- antes ou depois
		for(int contador = 20; contador >= 0; contador -= 2) {
			System.out.println("i = " + contador);
			
			
		}
		
		for (double contador1 = 21; contador1 >= -21; contador1 -= 2) {
			System.out.println("neymar e bao" + contador1);
		}
		
		
	}
}
